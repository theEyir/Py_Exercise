#   Comparison Operators

num_1 = input ("Enter num_1 :")
num_2 = input ("Enter num_2 :")

# ==
print(f" {num_1} == {num_2} : {num_1 == num_2}")

# !=
print(f" {num_1} != {num_2} : {num_1 != num_2}")

# >
print(f" {num_1} > {num_2} : {num_1 > num_2}")

# >=
print(f" {num_1} >= {num_2} : {num_1 >= num_2}")

# <
print(f" {num_1} < {num_2} : {num_1 < num_2}")

# <=
print(f" {num_1} <= {num_2} : {num_1 <= num_2}")


